// Helper function to get questions by category
export const getQuestionsByCategory = (category: Question['category']) =>
  questions.filter(q => q.category === category);

// Helper function to get random questions
export const getRandomQuestions = (count: number): Question[] => {
  const shuffled = [...questions].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

// Helper function to get random senior (65/20) questions
export const getSeniorQuestions = (count: number): Question[] => {
  const seniorQs = questions.filter(q => q.seniorQuestion);
  const shuffled = [...seniorQs].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

// Helper function to get a question by ID
export const getQuestionById = (id: number): Question | undefined =>
  questions.find(q => q.id === id);

